namespace Cibbi.CFAM;

/// <summary>
/// Position of the pane in a NavigationView
/// </summary>
public enum PanePosition
{
    /// <summary>
    /// The pane is shown on the left side of the control open state.
    /// </summary>
    Left,
    /// <summary>
    /// The pane is shown at the top of the control, ignores pane state.
    /// </summary>
    Top,
}